#include <stdio.h>

int main(){
	int a;
	scanf("%d",&a);
	if(a <= 100000) printf("%g",a*0.1);
	if(a > 100000 && a <= 200000) printf("%g", 10000 + (a - 100000) * 0.075);
	if(a > 200000 && a <= 400000) printf("%g", 10000 + 7500 + (a - 200000)*0.05);
	if(a > 400000 && a <= 600000) printf("%g", 10000+7500+10000+(a-400000)*0.03);
	if(a > 600000 && a <= 1000000) printf("%g", 10000+7500+10000+6000+(a-600000)*0.015);
	if(a > 1000000)printf("%g",10000+7500+10000+6000+6000+(a-1000000)*0.01);
	return 0;
}
